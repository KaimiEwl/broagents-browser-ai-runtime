export function getRolePrompt(role, runCard) {
  const objective =
    runCard?.objective || "Сделать систему стабильнее, понятнее и полезнее без лишнего шума.";

  if (role === "researcher") {
    return [
      "Роль: Исследователь.",
      `Контекст: ${objective}`,
      "Задача: найди 3 реальных варианта решения или улучшения.",
      "Смотри только на практичную пользу, надежность и быстрый эффект.",
      "",
      "Формат ответа:",
      "1. Вариант",
      "Плюс:",
      "Риск:",
      "",
      "В конце добавь строку: Рекомендация: ...",
      "Пиши коротко, по-русски, без воды."
    ].join("\n");
  }

  if (role === "critic") {
    return [
      "Роль: Критик.",
      `Контекст: ${objective}`,
      "Задача: проверь предложенное решение и найди слабые места.",
      "Ищи риски, скрытые издержки, дубли, ручные шаги и что может сломаться.",
      "",
      "Формат ответа:",
      "1. Главный риск",
      "Почему это важно:",
      "Как поправить:",
      "",
      "В конце добавь строку: Что оставить: ...",
      "Пиши коротко, по-русски, без воды."
    ].join("\n");
  }

  if (role === "synthesizer") {
    return [
      "Роль: Синтезатор.",
      `Контекст: ${objective}`,
      "Задача: собери финальный вывод и выбери один лучший следующий шаг.",
      "Опирайся только на полезные варианты и критику, не пересказывай все подряд.",
      "",
      "Формат ответа:",
      "Итог:",
      "Почему это лучший путь:",
      "Следующий шаг:",
      "Что не делать сейчас:",
      "",
      "Пиши коротко, по-русски, без воды."
    ].join("\n");
  }

  return "";
}
